package in.com.CollectionPractice;

public class Em2 {
	int id;
	String name;
	String phone;
	public Em2(int id, String name, String phone) {
		super();
		this.id = id;
		this.name = name;
		this.phone = phone;
	}
	@Override
	public String toString() {
		return "Em2 [id=" + id + ", name=" + name + ", phone=" + phone + "]";
	}
	

}
